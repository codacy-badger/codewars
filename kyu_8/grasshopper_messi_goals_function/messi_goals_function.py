def goals(laLiga: int, copaDelRey: int, championsLeague: int) -> int:
    """
    The function returns Messi's total number
    of goals in all three leagues:
    - LaLiga
    - Copa del Rey
    - Champions

    :param laLiga:
    :param copaDelRey:
    :param championsLeague:
    :return:
    """

    return laLiga + copaDelRey + championsLeague
